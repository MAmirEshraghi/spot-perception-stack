def extract_detection_data(entry_data: Dict[str, Any]) -> Dict[str, Dict]:
    """Extract detections from new JSON structure.
    
    Converts: entries[entry_key]["detections"][camera_name]
    To: {camera_name: {boxes, labels, confidences}}
    """
    detections = entry_data.get("detections", {})
    result = {}
    for camera_name, camera_data in detections.items():
        result[camera_name] = {
            "boxes": camera_data.get("boxes", []),
            "labels": camera_data.get("labels", []),
            "confidences": camera_data.get("confidences", []),
        }
    return result

def extract_pointing_data(entry_data: Dict[str, Any]) -> Dict[str, Dict]:
    """Extract pointing from new JSON structure.
    
    Converts: entries[entry_key]["points"][camera_name]
    To: {camera_name: {points, labels, confidences}}
    """
    points = entry_data.get("points", {})
    result = {}
    for camera_name, camera_data in points.items():
        result[camera_name] = {
            "points": camera_data.get("points", []),
            "labels": camera_data.get("labels", []),
            "confidences": camera_data.get("confidences", []),
        }
    return result

