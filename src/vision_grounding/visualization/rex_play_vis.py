def task_visualize(args) -> None:
    """Generate comparison visualization frames from JSON files."""
    output_dir = Path(args.output_dir)
    frame_dir = Path(args.frame_dir)
    # Ensure both directories exist
    output_dir.mkdir(parents=True, exist_ok=True)
    frame_dir.mkdir(parents=True, exist_ok=True)

    logger.info("Task: visualize")
    logger.info("Output directory: %s", output_dir)
    logger.info("Frame output directory: %s", frame_dir)

    # Auto-detect JSON files
    json_files = auto_detect_json_files(output_dir)
    
    if "vocab" not in json_files:
        raise FileNotFoundError("vocabulary_results.json not found. Run vocab task first.")
    if "yolo" not in json_files:
        raise FileNotFoundError("yolo_results.json not found. Run yolo task first.")
    if "rex_detection" not in json_files:
        raise FileNotFoundError("rex_omni_detection_results.json not found. Run rex_detection task first.")

    # Load JSON files
    logger.info("Loading JSON files...")
    vocab_data = load_json(json_files["vocab"])
    yolo_data = load_json(json_files["yolo"])
    rex_data = load_json(json_files["rex_detection"])
    
    # Load pointing data if available
    pointing_data = None
    if "rex_pointing" in json_files:
        logger.info("Loading pointing data...")
        pointing_data = load_json(json_files["rex_pointing"])
    else:
        logger.info("Pointing JSON not found, skipping pointing visualization")

    # Get pickle path from vocab metadata
    pickle_path = Path(vocab_data["metadata"]["pickle_path"])
    if not pickle_path.exists():
        logger.warning("Pickle path from metadata doesn't exist: %s", pickle_path)
        # Try relative to output dir
        pickle_path = output_dir.parent / pickle_path.name
        if not pickle_path.exists():
            raise FileNotFoundError(f"Pickle file not found: {pickle_path}")

    # Load pickle file
    entries = load_captured_buffer(pickle_path)

    # Process each entry
    total = 0
    saved = 0
    for entry_id, entry in entries.items():
        entry_key = str(entry_id)
        total += 1

        # Get entry data from JSONs
        vocab_entry = vocab_data.get("entries", {}).get(entry_key)
        yolo_entry = yolo_data.get("entries", {}).get(entry_key)
        rex_entry = rex_data.get("entries", {}).get(entry_key)

        if not (vocab_entry and yolo_entry and rex_entry):
            logger.warning(
                "Missing data for entry %s (vocab=%s, yolo=%s, rex=%s), skipping",
                entry_key,
                bool(vocab_entry),
                bool(yolo_entry),
                bool(rex_entry),
            )
            continue

        # Get images from pickle entry
        images_dict = getattr(entry, "rgb_images", None)
        if not images_dict:
            logger.warning("No RGB images for entry %s, skipping", entry_key)
            continue

        # Apply camera rotations
        images_dict = rotate_images_for_cameras(images_dict)

        # Extract data from new JSON structure
        vocabulary_by_camera = vocab_entry.get("vocabulary_by_camera", {})
        vocabulary = get_all_vocabulary(vocabulary_by_camera)
        
        yolo_results = extract_detection_data(yolo_entry)
        rex_omni_results = extract_detection_data(rex_entry)
        
        # Extract pointing if available
        pointing_results = None
        if pointing_data:
            pointing_entry = pointing_data.get("entries", {}).get(entry_key)
            if pointing_entry:
                pointing_results = extract_pointing_data(pointing_entry)

        # Create comparison grid
        grid = create_comparison_grid(
            yolo_results,
            rex_omni_results,
            images_dict,
            vocabulary,
            CAMERA_ORDER,
            vocabulary_by_camera,
            pointing_results,
        )

        # Save frame
        output_path = frame_dir / f"frame_{entry_key}.jpg"
        # create_comparison_grid returns RGB; OpenCV expects BGR.
        cv2.imwrite(str(output_path), cv2.cvtColor(grid, cv2.COLOR_RGB2BGR))
        saved += 1
        logger.info("Saved comparison frame: %s", output_path)

    logger.info("=" * 60)
    logger.info("Visualization complete. Frames saved: %d (processed entries: %d)", saved, total)
    logger.info("=" * 60)

