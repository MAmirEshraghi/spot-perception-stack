def auto_detect_json_files(output_dir: Path) -> Dict[str, Path]:
    """Auto-detect JSON files from output directory."""
    json_files = {
        "vocab": output_dir / "vocabulary_results.json",
        "yolo": output_dir / "yolo_results.json",
        "rex_detection": output_dir / "rex_omni_detection_results.json",
        "rex_pointing": output_dir / "rex_omni_pointing_results.json",
    }
    
    detected = {}
    for key, path in json_files.items():
        if path.exists():
            detected[key] = path
            logger.info("Found %s: %s", key, path)
        else:
            logger.warning("Missing %s: %s", key, path)
    
    return detected

