def create_comparison_grid(
    yolo_results: Dict[str, Dict],
    rex_omni_results: Dict[str, Dict],
    images_dict: Dict[str, np.ndarray],
    vocabulary: List[str],
    camera_order: List[str],
    vocabulary_by_camera: Dict[str, List[str]],
    pointing_results: Optional[Dict[str, Dict]] = None,
) -> np.ndarray:
    """Create side-by-side comparison grid for YOLO and Rex-Omni results with camera reports.
    
    Optionally includes a 4th row for pointing visualization if pointing_results is provided.
    """
    yolo_detected_vocab = set()
    yolo_total_detections = 0
    for cam_results in yolo_results.values():
        yolo_detected_vocab.update(cam_results.get("labels", []))
        yolo_total_detections += len(cam_results.get("boxes", []))
    yolo_detected_vocab = sorted(list(yolo_detected_vocab))

    rex_omni_detected_vocab = set()
    rex_omni_total_detections = 0
    for cam_results in rex_omni_results.values():
        rex_omni_detected_vocab.update(cam_results.get("labels", []))
        rex_omni_total_detections += len(cam_results.get("boxes", []))
    rex_omni_detected_vocab = sorted(list(rex_omni_detected_vocab))

    # Row 1: YOLO images + report
    yolo_images = []
    for camera_name in camera_order:
        if camera_name in images_dict:
            annotated = draw_boxes_on_image(images_dict[camera_name], yolo_results.get(camera_name, {}), camera_name, "YOLO")
        else:
            annotated = _create_missing_image_placeholder(camera_name)
        yolo_images.append(annotated)

    yolo_info = create_info_box(vocabulary, yolo_detected_vocab, "YOLO", INFO_BOX_SIZE[0], INFO_BOX_SIZE[1], yolo_total_detections)
    yolo_images.append(yolo_info)

    # Row 2: Rex-Omni images + report
    rex_omni_images = []
    for camera_name in camera_order:
        if camera_name in images_dict:
            annotated = draw_boxes_on_image(
                images_dict[camera_name],
                rex_omni_results.get(camera_name, {}),
                camera_name,
                "Rex-Omni",
            )
        else:
            annotated = _create_missing_image_placeholder(camera_name)
        rex_omni_images.append(annotated)

    rex_omni_info = create_info_box(vocabulary, rex_omni_detected_vocab, "Rex-Omni", INFO_BOX_SIZE[0], INFO_BOX_SIZE[1], rex_omni_total_detections)
    rex_omni_images.append(rex_omni_info)

    # Row 3: Camera reports + placeholder
    camera_reports = []
    for camera_name in camera_order:
        camera_vocab = vocabulary_by_camera.get(camera_name, [])
        yolo_labels = yolo_results.get(camera_name, {}).get("labels", [])
        rex_labels = rex_omni_results.get(camera_name, {}).get("labels", [])
        yolo_boxes_count = len(yolo_results.get(camera_name, {}).get("boxes", []))
        rex_boxes_count = len(rex_omni_results.get(camera_name, {}).get("boxes", []))
        
        report_box = _create_camera_report_box(
            camera_name,
            camera_vocab,
            yolo_labels,
            rex_labels,
            yolo_boxes_count,
            rex_boxes_count,
            GRID_IMAGE_SIZE[0],
            GRID_IMAGE_SIZE[1],
        )
        camera_reports.append(report_box)
    
    # Add placeholder for 6th box in row 3
    placeholder = np.ones((GRID_IMAGE_SIZE[1], GRID_IMAGE_SIZE[0], 3), dtype=np.uint8) * 255
    camera_reports.append(placeholder)

    # Stack base rows
    rows = [np.hstack(yolo_images), np.hstack(rex_omni_images), np.hstack(camera_reports)]
    
    # Row 4: Pointing images + report (optional)
    if pointing_results is not None:
        pointing_detected_vocab = set()
        pointing_total_points = 0
        for cam_results in pointing_results.values():
            pointing_detected_vocab.update(cam_results.get("labels", []))
            pointing_total_points += len(cam_results.get("points", []))
        pointing_detected_vocab = sorted(list(pointing_detected_vocab))
        
        pointing_images = []
        for camera_name in camera_order:
            if camera_name in images_dict:
                annotated = draw_points_on_image(
                    images_dict[camera_name],
                    pointing_results.get(camera_name, {}),
                    camera_name,
                )
            else:
                annotated = _create_missing_image_placeholder(camera_name)
            pointing_images.append(annotated)
        
        pointing_info = create_info_box(
            vocabulary,
            pointing_detected_vocab,
            "Pointing",
            INFO_BOX_SIZE[0],
            INFO_BOX_SIZE[1],
            pointing_total_points,
        )
        pointing_images.append(pointing_info)
        rows.append(np.hstack(pointing_images))
    
    grid = np.vstack(rows)
    return grid

