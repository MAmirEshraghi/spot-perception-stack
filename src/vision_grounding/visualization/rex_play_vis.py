def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Detection Pipeline Visualization")
    parser.add_argument(
        "--output-dir",
        type=str,
        default=str(DEFAULT_OUTPUT_DIR),
        help=f"Base output directory where JSON files are located (default: {DEFAULT_OUTPUT_DIR})",
    )
    parser.add_argument(
        "--frame-dir",
        type=str,
        default=str(DEFAULT_FRAME_DIR),
        help=f"Directory to save visualization frames (default: {DEFAULT_FRAME_DIR})",
    )
    return parser

def main(argv=None) -> None:
    parser = build_parser()
    args = parser.parse_args(argv)

    task_visualize(args)

if __name__ == "__main__":
    main()

