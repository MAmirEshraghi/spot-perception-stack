def generate_distinct_colors(n):
    """Generates n visually distinct colors."""
    colors = []
    for i in range(n):
        # These values are chosen to create a good spread across the color spectrum
        hue = i * 0.61803398875  # Golden ratio ensures good separation
        hue %= 1
        # Simple HSV to RGB conversion
        if hue < 1/6.:
            r, g, b = 1, hue*6, 0
        elif hue < 2/6.:
            r, g, b = 1-(hue-1/6.)*6, 1, 0
        elif hue < 3/6.:
            r, g, b = 0, 1, (hue-2/6.)*6
        elif hue < 4/6.:
            r, g, b = 0, 1-(hue-3/6.)*6, 1
        elif hue < 5/6.:
            r, g, b = (hue-4/6.)*6, 0, 1
        else:
            r, g, b = 1, 0, 1-(hue-5/6.)*6
        
        colors.append((int(r*255), int(g*255), int(b*255)))
    return colors
