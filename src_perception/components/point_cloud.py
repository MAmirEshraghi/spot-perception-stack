def get_o3d_cam_intrinsic(height, width):
    """Returns the Open3D camera intrinsic object based on image size."""
    fx = width / 2.0
    fy = width / 2.0
    cx = width / 2.0
    cy = height / 2.0
    return o3d.camera.PinholeCameraIntrinsic(width, height, fx, fy, cx, cy)
